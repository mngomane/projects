#include "push_swap.h"

t_option	get_swap_option(char **av)
{
	if (av[1][0] && av[1][0] != '-')
		return (NONE);
	if (av[1][1] && av[1][1] == 'd' && av[1][2] == '\0')
		return (DEBUG);
	return (NONE);
}
