#include "ft_math.h"

size_t		ft_next_pow2(size_t x)
{
	size_t	bit;

	bit = 1;
	x--;
	while (bit < 8 * sizeof(long))
	{
		x |= x >> bit;
		bit <<= 1;
	}
	x++;
	return (x);
}
